package pack.room;

public class LodgingDto {
	private String lodging_name, lodging_loc, lodging_img, ceo_id, room_image;
	private int lodging_no, facility_no;
	
	public String getLodging_name() {
		return lodging_name;
	}
	public void setLodging_name(String lodging_name) {
		this.lodging_name = lodging_name;
	}
	public String getLodging_loc() {
		return lodging_loc;
	}
	public void setLodging_loc(String lodging_loc) {
		this.lodging_loc = lodging_loc;
	}
	public String getLodging_img() {
		return lodging_img;
	}
	public void setLodging_img(String lodging_img) {
		this.lodging_img = lodging_img;
	}
	public String getCeo_id() {
		return ceo_id;
	}
	public void setCeo_id(String ceo_id) {
		this.ceo_id = ceo_id;
	}
	public int getLodging_no() {
		return lodging_no;
	}
	public void setLodging_no(int lodging_no) {
		this.lodging_no = lodging_no;
	}
	public int getFacility_no() {
		return facility_no;
	}
	public void setFacility_no(int facility_no) {
		this.facility_no = facility_no;
	}
	public String getRoom_image() {
		return room_image;
	}
	public void setRoom_image(String room_image) {
		this.room_image = room_image;
	}
	
}
