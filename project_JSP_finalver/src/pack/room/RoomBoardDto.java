package pack.room;

public class RoomBoardDto {
	private String rbtitle, cont1, cont2, img;
	private int num, roomcnt, room_no;
	
	public String getRbtitle() {
		return rbtitle;
	}
	public void setRbtitle(String rbtitle) {
		this.rbtitle = rbtitle;
	}
	public String getCont1() {
		return cont1;
	}
	public void setCont1(String cont1) {
		this.cont1 = cont1;
	}
	public String getCont2() {
		return cont2;
	}
	public void setCont2(String cont2) {
		this.cont2 = cont2;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getRoomcnt() {
		return roomcnt;
	}
	public void setRoomcnt(int roomcnt) {
		this.roomcnt = roomcnt;
	}
	public int getRoom_no() {
		return room_no;
	}
	public void setRoom_no(int room_no) {
		this.room_no = room_no;
	}
	
}
